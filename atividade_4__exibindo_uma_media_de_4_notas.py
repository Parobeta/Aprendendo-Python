# Exibindo a média de quatro médias

print("Digite 4 notas, ao digitar a quarta nota será exibido a média das notas.")
num1 = float(input("Digite a primeira nota: "))
num2 = float(input("Digite a segunda nota: "))
num3 = float(input("Digite a terceira nota: "))
num4 = float(input("Digite a quarta nota: "))
soma = num1 + num2 + num3 + num4
media = soma / 4
print("A média dessas 4 notas é: ", media)